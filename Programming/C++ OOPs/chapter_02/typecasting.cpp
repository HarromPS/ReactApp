#include<iostream>
#include<cstdio>
using namespace std;

int main(){

    int a=2323;
    cout<<"a is now a float "<<(float)a<<endl;

    float b=23.34;
    cout<<"b is now a int "<<(int)b<<endl;

    char c='c';
    // c will displayed as 99, c=99 A/c to ASCII
    cout<<"c is now a int "<<(int)c<<endl;

    return 0; 
}