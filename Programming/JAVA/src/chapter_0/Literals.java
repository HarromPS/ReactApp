package chapter_0;

public class Literals {
    public static void main(String[] args) {
        byte age = 23;
        // int age2 = 121;
        // short st = 23;
        char ch = 'A';
        float f1 = 2.33F; // or 2.33f means floating point literal
        double d1 = 65.32; // default is double literal
        // double d1=65.32D; // default is double literal

        // long x = 122L; // long
        // boolean a = true;
        // boolean a1 = false;

        // String Literal
        String str="Hello";
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(str);

    }
    /*
    Keywords are the reserved words which is used by the java compilers & 
    cannot be used as an identifiers.
    */
}
