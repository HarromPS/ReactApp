// Defining macros for Area of and Perimter of Square
//          (side*side) ==> This is a return value
#define AREA_SQ(side) (side*side) 
#define PERIMETER_SQ(side) (4*side)

// Defining macros for Area of and Perimter of Circle
#define PI 3.142
#define AREA_CIRCLE(radius) (PI*radius*radius)
#define PERIMETER_CIRCLE(radius) (2*PI*radius)

// Defining macros for Area of and Perimter of Triangle
#define AREA_TRIANGLE(base,height) (0.5*base*height)
#define PERIMETER_TRIANGLE(a,b,c) (a+b+c)
