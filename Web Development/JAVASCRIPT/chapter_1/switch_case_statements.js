// Switch case statements 
let a = 11;
switch (a) {
    case 10:
        console.log("You are", a, "years old");
        break;
    case 11:
        console.log("You are", a, "years old");
        break;
    case 12:
        console.log("You are", a, "years old");
        break;
    default:
        console.log("Invalid age");
        break;
}